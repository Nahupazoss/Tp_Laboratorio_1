int validarNumero(char numero[])//crear funcion
{
    int retorno=1;
    int len;
    if(numero!=NULL){
        len=strlen(numero);
        for(int i=0;i<len;i++){ //"51"
            if(!isdigit(numero[i])){
                retorno=0;
                break;
            }
        }
    }
    return retorno;
}
