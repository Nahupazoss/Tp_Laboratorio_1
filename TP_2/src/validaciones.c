int validarChar(char palabra[])//crear funcion
{
    int retorno=1;
    int len;
    if(palabra!=NULL){
        len=strlen(palabra);
        for(int i=0;i<len;i++){ //"51"
            if(!isalpha(palabra[i]))
            {
                retorno=0;
                break;
            }
        }
    }
    return retorno;
}
